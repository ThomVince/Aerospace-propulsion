import numpy as np
from scipy.interpolate import interp1d

def clarkypolars(aoa):
    # Wrap angle of attack to the range [-π, π]
    aoa = np.arctan2(np.sin(aoa), np.cos(aoa))

    # Hard-coded polar values
    polar_data = polardata()

    # Interpolation to get cl and cd
    cl_interp = interp1d(polar_data[:, 0], polar_data[:, 1], kind='linear', fill_value="extrapolate")
    cd_interp = interp1d(polar_data[:, 0], polar_data[:, 2], kind='linear', fill_value="extrapolate")

    cl = cl_interp(aoa)
    cd = cd_interp(aoa)

    return cl, cd

